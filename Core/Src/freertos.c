void StartTask02(void const * argument)
{
  /* USER CODE BEGIN StartTask02 */
  /* Infinite loop */
  for(;;)
  {
	  uint8_t  tbuf[17],*ST, *AT;
	  unsigned int  start;

		start = 0x08000000;
		tbuf[16] = '\0';

		osDelay(1);
	 	printf(" \r\n\n");
		osDelay(1);
		printf(" Address : 00 01 02 03 04 05 06 07  08 09 0A 0B 0C 0D 0E 0F \r\n");
		osDelay(1);
	 	printf(" ========  =======================  ======================= \r\n");
		AT = ST = (uint8_t*) start;

  		for(int j=0; j<16; j++){
			printf("\r\n %08x: ",start);

			prt_hexbnb(8,ST);
		//		printf("-");
			ST += 8;

			prt_hexbnb(8,ST);
			ST += 8;

	 	 	for(uint8_t i=0; i<16; i++){
				if(*AT >= 0x20 && *AT < 0x7f){
					tbuf[i] = *AT;
				}
				else {
					tbuf[j] = '.';
				}
				AT++;
	 		}
	 	 	printf("|");
			printf((char*)tbuf);
		start += 16;
		  		}

		printf("\r\n\n");
    osDelay(1000);
  }
  /* USER CODE END StartTask02 */
}

