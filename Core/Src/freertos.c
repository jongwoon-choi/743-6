void prt_hexbnb(int n, uint8_t *hex)
{
	byte buf[50],c,i;

	i=0;
	while(n--){
		c = *hex/16;
		if(c>9) {
			c +=7;
		}
		buf[i++] = c + '0';
		c=(*hex & 0xf);
		if(c>9){
			c +=7;
		}
		buf[i++]= c +'0';
		buf[i++]=' ';
		hex++;
	}
	buf[i++]=' ';
	buf[i] = '\0';
	printf((char*)buf);

}
